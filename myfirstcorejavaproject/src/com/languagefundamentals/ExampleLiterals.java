package com.languagefundamentals;

public class ExampleLiterals {

	public static void main(String[] args) {
		double d1 = 100;
		double d2 = 0123;
		double d3 = 123F;
		double d4 = 123.5;
		double d5 = 123.5F;
		double d6 = 0x123;
		double d7 = 0123.5;
		double d8 = 0123.5F; 
		double d9 = 0x123F;
		double d10 = 1230.5F;
		
		System.out.println(d1);//100.0
		System.out.println(d2);//83.0
		System.out.println(d3);//123.0
		System.out.println(d4);//123.5
		System.out.println(d5);//123.5
		System.out.println(d6);//291.0
		System.out.println(d7);//123.5
		System.out.println(d8);//123.5
		System.out.println(d9);//4671.0
		System.out.println(d10);//1230.5
	}

}
